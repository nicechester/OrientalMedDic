def make_msg(user_content, assistant_content):
    return {
        "messages": [
            {"role": "system", "content": SYSTEM},
            {"role": "user", "content": user_content},
            {"role": "assistant", "content": assistant_content},
        ]
    }


def gen_single_char_readings():
    """단일 한자 독음 학습"""
    samples = []
    for char, reading in MEDICAL_READINGS.items():
        # 패턴 1: 독음만
        samples.append(make_msg(
            f"다음 한자의 독음을 알려줘.\n{char}",
            f"[독음] {reading}"
        ))
        # 패턴 2: 의서 맥락에서
        samples.append(make_msg(
            f"다음 한문 의서 구절을 해석해줘.\n{char}",
            f"[독음] {reading}"
        ))
    return samples


def gen_compound_readings():
    """복합어 독음 학습"""
    samples = []
    for compound, reading in COMPOUND_READINGS.items():
        samples.append(make_msg(
            f"다음 한문 의서 구절을 해석해줘.\n{compound}",
            f"[독음] {reading}"
        ))
        samples.append(make_msg(
            f"다음 한자의 독음을 알려줘.\n{compound}",
            f"[독음] {reading}"
        ))
    return samples


def gen_sentence_readings():
    """문장 독음 학습 (가장 중요)"""
    samples = []
    for orig, reading in SENTENCE_READINGS:
        # 패턴 1: 독음 + 해설 요청
        samples.append(make_msg(
            f"다음 한문 의서 구절을 해석해줘.\n{orig}",
            f"[독음] {reading}"
        ))
        # 패턴 2: 독음만 요청
        samples.append(make_msg(
            f"다음 한문의 독음을 달아줘.\n{orig}",
            f"[독음] {reading}"
        ))
        # 패턴 3: 변형
        samples.append(make_msg(
            f"독음을 포함해서 해석해줘.\n{orig}",
            f"[독음] {reading}"
        ))
    return samples


def gen_random_combinations():
    """랜덤 조합으로 독음 학습 데이터 생성"""
    samples = []
    compounds = list(COMPOUND_READINGS.items())

    for _ in range(500):
        # 2~4개 복합어 조합
        n = random.randint(2, 4)
        selected = random.sample(compounds, min(n, len(compounds)))
        orig = " ".join(c[0] for c in selected)
        reading = " ".join(c[1] for c in selected)

        samples.append(make_msg(
            f"다음 한문 의서 구절을 해석해줘.\n{orig}",
            f"[독음] {reading}"
        ))

    return samples


def main():
    all_samples = []

    s1 = gen_single_char_readings()
    print(f"단일 한자 독음: {len(s1)}건")
    all_samples.extend(s1)

    s2 = gen_compound_readings()
    print(f"복합어 독음: {len(s2)}건")
    all_samples.extend(s2)

    s3 = gen_sentence_readings()
    print(f"문장 독음: {len(s3)}건")
    all_samples.extend(s3)

    s4 = gen_random_combinations()
    print(f"랜덤 조합: {len(s4)}건")
    all_samples.extend(s4)

    random.shuffle(all_samples)

    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        for s in all_samples:
            f.write(json.dumps(s, ensure_ascii=False) + "\n")

    print(f"\n총 {len(all_samples)}건 → {OUTPUT_PATH}")
    print(f"파일 크기: {OUTPUT_PATH.stat().st_size / 1024:.1f}KB")


if __name__ == "__main__":
    main()
